char	*ft_strrchr(char *str, char c)
{
	int	i;
	int	j;

	i = 0;
	j = -1;
	while (str[i] != '\0')
	{
		if (str[i] == c)
			j = i;
		i++;
	}
	if (str[i] == c)
		j = i;
	if (j == -1)
		return (0);
	return (&str[j]);
}
